import numpy as np

arr1=np.array([7,9,6,8,5])
arr2=np.array([8,3,2,5,6])


print("Addition of two arrays: ",arr1+arr2)
print("Subtraction of two arrays: ",arr1-arr2)
print("Multiplication of two arrays: ",arr1*arr2)
print("Division of two arrays: ",arr1/arr2) 
print("power of two arrays: ",arr1**arr2)